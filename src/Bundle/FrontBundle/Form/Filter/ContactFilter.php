<?php
namespace Bundle\FrontBundle\Form\Filter;

use WpThemeKitLib\Form\Filter\Filter;

class ContactFilter extends Filter
{
    public function __construct() {

        $this->add(array(
            'name' => 'name',
            'required' => true,
            'filters' => array(
                array('name' => 'StripTags'),
                array('name' => 'StringTrim'),
            ),
        ));

        $this->add(array(
            'name' => 'message',
            'required' => true,
            'filters' => array(
                array('name' => 'StripTags'),
                array('name' => 'StringTrim'),
            ),
            'validators' => array(
                array(
                    'name' => 'StringLength',
                    'options' => array(
                        'min' => 5,
                        'max' => 100,
                    ),
                ),
            ),
        ));
    }
}
